export const locales = ['zh', 'ja'] as const;

export type Locale = (typeof locales)[number];

export const defaultLocale: Locale = 'ja';

const localeLabels: Record<Locale, string> = {
  zh: '中文',
  ja: '日本語',
};

const trimTrailingSlash = (path: string) =>
  path.length > 1 && path.endsWith('/') ? path.slice(0, -1) : path;

export function getLocaleFromPath(pathname: string): Locale {
  const firstSegment = pathname.split('/').filter(Boolean)[0];
  if (firstSegment === 'zh') {
    return 'zh';
  }
  return 'ja';
}

export function stripLocaleFromPath(pathname: string): string {
  const normalizedPath = trimTrailingSlash(pathname || '/');
  if (normalizedPath === '/ja' || normalizedPath === '/zh') {
    return '/';
  }
  if (normalizedPath.startsWith('/ja/') || normalizedPath.startsWith('/zh/')) {
    return normalizedPath.replace(/^\/(?:ja|zh)/, '') || '/';
  }
  return normalizedPath || '/';
}

export function localizePath(pathname: string, locale: Locale): string {
  const basePath = stripLocaleFromPath(pathname);
  if (locale === defaultLocale) {
    return basePath;
  }
  return basePath === '/' ? '/zh' : `/zh${basePath}`;
}

export function getLanguageLinks(pathname: string, currentLocale: Locale) {
  return locales.map((locale) => ({
    locale,
    label: localeLabels[locale],
    href: localizePath(pathname, locale),
    current: locale === currentLocale,
  }));
}

