export type I18nContent = (typeof i18n)[Locale];

export function getI18n(pathnameOrLocale: string | Locale): I18nContent {
  const locale = locales.includes(pathnameOrLocale as Locale)
    ? (pathnameOrLocale as Locale)
    : getLocaleFromPath(pathnameOrLocale);
  return i18n[locale];
}
